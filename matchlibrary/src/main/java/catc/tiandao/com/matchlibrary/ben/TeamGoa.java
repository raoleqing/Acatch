public class TeamGoa {

    //{\"teamName\":\"哥德堡\",\"count\":5,\"min\":15}
    private String teamName;
    private int count;
    private int min;

    public String getTeamName() {
        return teamName;
    }

    public void setTeamName(String teamName) {
        this.teamName = teamName;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }

    public int getMin() {
        return min;
    }

    public void setMin(int min) {
        this.min = min;
    }
}
